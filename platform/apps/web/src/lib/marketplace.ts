// Formatters — kept here so cards and tables render numbers the same way.
// ---------------------------------------------------------------------------

export function formatUsd(n: number, opts: { compact?: boolean } = {}): string {
  if (!Number.isFinite(n)) return "—";
  if (opts.compact) {
    const abs = Math.abs(n);
    if (abs >= 1_000_000) return `$${(n / 1_000_000).toFixed(2)}M`;
    if (abs >= 1_000) return `$${(n / 1_000).toFixed(1)}K`;
  }
  const digits = Math.abs(n) < 1 ? 4 : 2;
  return `$${n.toFixed(digits)}`;
}

export function formatPct(n: number): string {
  const pct = n * 100;
  const sign = pct > 0 ? "+" : "";
  return `${sign}${pct.toFixed(2)}%`;
}

export function formatNumber(n: number): string {
  if (!Number.isFinite(n)) return "—";
  const abs = Math.abs(n);
  if (abs >= 1_000_000) return `${(n / 1_000_000).toFixed(2)}M`;
  if (abs >= 1_000) return `${(n / 1_000).toFixed(1)}K`;
  return n.toFixed(0);
}

export function timeAgo(ts: number): string {
  const diff = Math.max(0, Date.now() - ts);
  const s = Math.floor(diff / 1000);
  if (s < 60) return `${s}s ago`;
  const m = Math.floor(s / 60);
  if (m < 60) return `${m}m ago`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h}h ago`;
  return `${Math.floor(h / 24)}d ago`;
}

export function sortListings(
  listings: AgentListing[],
  key: SortKey,
): AgentListing[] {
  const copy = [...listings];
  switch (key) {
    case "revenue":
      return copy.sort(
        (a, b) => b.performance.revenue24hUsd - a.performance.revenue24hUsd,
      );
    case "missions":
      return copy.sort(
        (a, b) => b.performance.missions24h - a.performance.missions24h,
      );
    case "price":
      return copy.sort((a, b) => b.price.usd - a.price.usd);
    case "new":
      return copy.sort((a, b) => b.updatedAt - a.updatedAt);
    case "momentum":
    default:
      return copy.sort(
        (a, b) =>
          b.price.change24h * b.performance.successRate -
          a.price.change24h * a.performance.successRate,
      );
  }
}
