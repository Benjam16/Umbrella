/**
 * Umbrella Agent Marketplace — shared types + seed data.
 *
 * Every `AgentListing` is a token backed by the *labor* of a specific Umbrella
 * agent. The price isn't driven by narrative — it's driven by a
 * Performance-Oracle Loop:
 *
 *   mission completes → RelayerService signs a proof →
 *   UmbrellaAgentToken.recordSuccess() → v4 hook adjusts fee & buyback rate
 *
 * Until the relayer + v4 hook are deployed, these listings are generated
 * client/server-side so we can design against the final shape. The JSON schema
 * here is what the real API will return on day one — swap the data source,
 * nothing else.
 */

export type AgentCategory =
  | "research"
  | "trading"
  | "yield"
  | "scraper"
  | "writer"
  | "security";

export type MissionEvent = {
  /** Umbrella run id. */
  runId: string;
  /** Epoch ms when the mission completed. */
  ts: number;
  /** 0.0–1.0 success score from the supervisor. */
  success: number;
  /** Net revenue in USD generated by this mission. */
  revenueUsd: number;
  /** Short human blurb that appears in the "Proof of Work" ticker. */
  label: string;
};

export type PriceTick = {
  /** Epoch ms. */
  t: number;
  /** Token price in USD. */
  price: number;
};

export type AgentListing = {
  id: string;
  /** Token ticker, e.g. "YIELD". */
  symbol: string;
  /** Human name, e.g. "Yield Reaper". */
  name: string;
  tagline: string;
  category: AgentCategory;
  blueprintId: string;
  /** ERC-8004 agent identity NFT contract + tokenId. */
  identity: { chain: "base"; contract: string; tokenId: string };
  /** ERC-20 labor-backed token, on Base. */
  token: { chain: "base"; address: string; decimals: number };
  /** Uniswap v4 pool + hook responsible for the productivity economics. */
  pool: { id: string; hookAddress: string; tvlUsd: number; volume24hUsd: number };
  price: {
    usd: number;
    change24h: number;
    change7d: number;
    fdvUsd: number;
  };
  /** Labor metrics — these are what the v4 hook reads. */
  performance: {
    missionsTotal: number;
    missions24h: number;
    successRate: number; // 0–1
    revenueAllTimeUsd: number;
    revenue24hUsd: number;
    /** Tokens bought-back-and-burned by the agent from its own earnings. */
    burnedTokens: number;
    /** Live dynamic swap fee set by the hook, in bps (1 bp = 0.01%). */
    dynamicFeeBps: number;
    /** Runway in hours before the agent's Smart Wallet runs dry. */
    runwayHours: number;
    /** Whether the agent is currently executing a mission. */
    active: boolean;
  };
  spark: PriceTick[];
  missions: MissionEvent[];
  /** Unix ms that this listing was last updated by the relayer. */
  updatedAt: number;
  /**
   * Number of times this hook has been forked by other users (via the
   * Marketplace "Fork" flow). Populated by `/api/v1/marketplace` from the
   * `forked_from` column added in migration 0003.
   */
  forksCount?: number;
  /**
   * Pump.fun-style bonding curve metadata. Populated once the launch
   * orchestrator has deployed the curve. Stages:
   *   - "pending"    : launch underway, curve not yet live
   *   - "deploying"  : factory tx seen, curve deploy in progress
   *   - "active"     : curve trading is open
   *   - "graduated"  : threshold crossed; v4 pool is live
   *   - "failed"     : deploy failed, surfaced as deploy_error
   */
  curve?: {
    address: string | null;
    chainId: number | null;
    stage: "pending" | "deploying" | "active" | "graduated" | "failed";
    ethReserveWei: string;
    graduationThresholdWei: string;
    progress: number;
    deployError: string | null;
    verifiedAt: string | null;
  };
};

