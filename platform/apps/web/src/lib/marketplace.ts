// ---------------------------------------------------------------------------
// Seed generator — deterministic so the UI is stable across reloads. Builds
// a plausibly noisy price walk and correlates "mission complete" markers with
// small positive jumps.
// ---------------------------------------------------------------------------

function mulberry32(seed: number): () => number {
  let a = seed >>> 0;
  return () => {
    a = (a + 0x6d2b79f5) >>> 0;
    let t = a;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function hashStr(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function buildSpark(
  seed: number,
  basePrice: number,
  points: number,
  missions: MissionEvent[],
): PriceTick[] {
  const rng = mulberry32(seed);
  const now = Date.now();
  const ticks: PriceTick[] = [];
  let price = basePrice;
  const missionBumps = new Set(
    missions.map((m) => Math.floor(((now - m.ts) / (24 * 60 * 60 * 1000)) * points)),
  );
  for (let i = points - 1; i >= 0; i--) {
    const drift = (rng() - 0.48) * 0.04;
    const bump = missionBumps.has(points - 1 - i) ? 0.01 + rng() * 0.03 : 0;
    price = Math.max(0.0001, price * (1 + drift + bump));
    ticks.push({ t: now - i * (24 * 60 * 60 * 1000) / points, price });
  }
  return ticks;
}

function buildMissions(
  seed: number,
  count: number,
  tone: string[],
): MissionEvent[] {
  const rng = mulberry32(seed);
  const now = Date.now();
  const out: MissionEvent[] = [];
  for (let i = 0; i < count; i++) {
    const hoursAgo = rng() * 24;
    out.push({
      runId: Math.random().toString(16).slice(2, 10),
      ts: now - Math.floor(hoursAgo * 60 * 60 * 1000),
      success: 0.6 + rng() * 0.4,
      revenueUsd: Math.round(rng() * 60 * 100) / 100,
      label: tone[Math.floor(rng() * tone.length)],
    });
  }
  return out.sort((a, b) => b.ts - a.ts);
}

